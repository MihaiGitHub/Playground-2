class Node {
  constructor(data) {
    this.data = data;
    this.children = [];
  }

  add(data) {
    this.children.push(new Node(data));
  }

  remove(data) {
    this.children = this.children.filter((node) => {
      // keep in array copy every everything that is not equal to argument
      return node.data !== data;
    });
  }
}
